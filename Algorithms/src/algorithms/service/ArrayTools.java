package algorithms.service;

public class ArrayTools {
	
	public static void printArr(int[] arr) {
		System.out.print("현재 배열: ");
		for(int i=0; i<arr.length; i++){
			System.out.print(arr[i] + " ");
		}
		System.out.println();
	}

	public static void printArr(char[] arr) {
		System.out.print("현재 배열: ");
		for(int i=0; i<arr.length; i++){
			System.out.print(arr[i] + " ");
		}
		System.out.println();
	}
	
}
